public class Position {
    private int x;
    private int y;

    public Position() {
        return;
    }

    public int getx() {
        return x;
    }

    public int gety() {
        return y;
    }

    public void setx(int newX) {
        x = newX;
    }

    public void sety(int newY) {
        y = newY;
    }
}
